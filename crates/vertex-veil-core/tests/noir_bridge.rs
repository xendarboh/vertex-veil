#[test]
fn noir_bridge_requester_tampered_round_fails_execute() {
    let circuit = load_requester();
    let intent = req_intent(500, "GPU");
    let nonce = [0xa1; 32];
    // Build commitment bound to round 0.
    let (mut public, _commit) =
        RequesterCircuit::public_inputs_from_intent(&intent, &nonce, RoundId::new(0), 250)
            .unwrap();
    // Tamper the public round input.
    public.round = RoundId::new(1);
    let witness = RequesterWitness {
        budget_cents: *intent.budget_cents.expose(),
        nonce,
    };
    let err = circuit.execute(&public, &witness).unwrap_err();
    // Error message should not contain the (private) budget value.
    let msg = format!("{err}");
    assert!(!msg.contains("500"), "error leaked private budget: {msg}");
}

#[test]
fn noir_bridge_requester_tampered_commitment_fails_execute() {
    let circuit = load_requester();
    let intent = req_intent(500, "GPU");
    let nonce = [0xa1; 32];
    let (mut public, _commit) =
        RequesterCircuit::public_inputs_from_intent(&intent, &nonce, RoundId::new(0), 250)
            .unwrap();
    // Flip a single byte of the published commitment hash.
    public.commitment_hash[0] ^= 0x01;
    let witness = RequesterWitness {
        budget_cents: *intent.budget_cents.expose(),
        nonce,
    };
    assert!(circuit.execute(&public, &witness).is_err());
}

